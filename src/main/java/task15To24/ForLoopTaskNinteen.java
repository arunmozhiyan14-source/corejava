package task15To24;

/*Task 19: Print 1 to 50
Use a for loop to print all numbers from 1 to 50.
*/
public class ForLoopTaskNinteen {

    public void printingOneToFifty(){
        for(int i=1;i<=50;i++){
            System.out.println(i);
        }
    }

    public static void main(String[] args) {
        ForLoopTaskNinteen task19 = new ForLoopTaskNinteen();
        task19.printingOneToFifty();
    }
}
